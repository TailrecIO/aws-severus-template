public class NumberAB {

    private int a;

    private int b;

    public NumberAB(){}

    public NumberAB(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

}
